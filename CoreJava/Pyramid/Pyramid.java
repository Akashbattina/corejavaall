package Pyramid;

 class Pyramid {
	
		  public static void main(String[]args){
		  for(int i=1;i<=3;i++){
		  for(int j=i;j<=3;j--){
		  System.out.println("");
		  }
		  for(int j=1;j<=i;j++){
		  System.out.println(""+i);
		  }
		 System.out.println();
		 }
	     for(int i=1;i<=2;i++){
		 for(int j=i;j<=2;j++){
		 System.out.println("");
		
		 }
		 for(int j=1;j<=i;j++){
		 System.out.println();
		 }
		 for(int i1=1;i1<=4;i1++){
		 for(int j=i1;j<=4;j++){
		 System.out.println("");
		
		 }
		 for(int j=1;j<=i1;j++){
		 System.out.println(""+i1);
		 }
		 System.out.println();
}
}
		  }
 }